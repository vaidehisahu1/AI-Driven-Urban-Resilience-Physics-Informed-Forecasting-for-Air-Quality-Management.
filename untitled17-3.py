location_stats = df.groupby('location')[['temp_c', 'humidity', 'windspeed_kph', 'pm2_5', 'co', 'no2', 'lon', 'lat']].mean().reset_index()

# 2. Create "Urban Stress Index" (USI)
# Logic: High Pollution + High Temp + Low Wind = High Stress (Unhealthy Metabolism)
# We normalize these factors to create a 0-100 score.
# Note: We invert wind speed because low wind = higher stress.

# Normalize key parameters (0 to 1)
location_stats['stress_pollution'] = location_stats['pm2_5'] / location_stats['pm2_5'].max()
location_stats['stress_heat'] = location_stats['temp_c'] / location_stats['temp_c'].max()
location_stats['stress_stagnation'] = 1 - (location_stats['windspeed_kph'] / location_stats['windspeed_kph'].max()) # Inverse

# Calculate Composite Score (Weighted Average)
# We weight pollution highest as it's critical for health.
location_stats['urban_stress_index'] = (
    (location_stats['stress_pollution'] * 0.5) +
    (location_stats['stress_heat'] * 0.3) +
    (location_stats['stress_stagnation'] * 0.2)
) * 100

# 3. AI Clustering for Zoning (K-Means)
# We use AI to automatically group locations based on their characteristics
# Features for clustering: Temp, Wind, PM2.5, NO2 (Traffic proxy)
X_cluster = location_stats[['temp_c', 'windspeed_kph', 'pm2_5', 'no2']].values

# Initialize KMeans to find 3 types of zones
kmeans = KMeans(n_clusters=3, random_state=42)
location_stats['zone_type'] = kmeans.fit_predict(X_cluster)

# Define Zone Labels based on cluster centers logic
# (Logic: 0=Stagnant/HighPoll, 1=Moderate, 2=Ventilated or similar - adjusted by data)
cluster_map = {}
for i in range(3):
    center = kmeans.cluster_centers_[i]
    # Logic: High PM2.5 + Low Wind = Stagnation Zone
    if center[2] > location_stats['pm2_5'].mean() and center[1] < location_stats['windspeed_kph'].mean():
        cluster_map[i] = "Stagnation Zone (High Risk)"
    # Logic: High NO2 = Traffic Hotspot
    elif center[3] > location_stats['no2'].mean():
        cluster_map[i] = "Traffic Hotspot (Emission Source)"
    else:
        cluster_map[i] = "Ventilation Corridor (Lower Risk)"

location_stats['zone_label'] = location_stats['zone_type'].map(cluster_map)

