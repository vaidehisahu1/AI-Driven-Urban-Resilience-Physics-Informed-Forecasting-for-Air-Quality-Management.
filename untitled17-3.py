import torch.nn as nn

class PollutionLSTM(nn.Module):
    def __init__(self, input_size, hidden_size, num_layers, output_size):
        super(PollutionLSTM, self).__init__()
        self.hidden_size = hidden_size
        self.num_layers = num_layers

        # The LSTM layer
        # input_size = 8 (number of features)
        # hidden_size = 64 (internal memory size)
        # num_layers = 2 (depth of the network)
        self.lstm = nn.LSTM(input_size, hidden_size, num_layers, batch_first=True, dropout=0.2)

        # Fully connected layer to map hidden state to output (PM2.5)
        self.fc = nn.Linear(hidden_size, output_size)

    def forward(self, x):
        # Initialize hidden state and cell state with zeros
        h0 = torch.zeros(self.num_layers, x.size(0), self.hidden_size).to(device)
        c0 = torch.zeros(self.num_layers, x.size(0), self.hidden_size).to(device)

        # Forward propagation through LSTM
        # out shape: (batch_size, seq_length, hidden_size)
        out, _ = self.lstm(x, (h0, c0))

        # Decode the hidden state of the last time step
        # We only care about the output of the last hour in the sequence to predict the next hour
        out = self.fc(out[:, -1, :])
        return out

# --- Physics-Informed Loss Function ---
def physics_loss(pred, target, inputs):
    # 1. Standard Mean Squared Error (Data Fidelity)
    # This forces the model to match the actual sensor readings
    mse_loss = nn.MSELoss()(pred, target)

    # 2. Physical Constraint: Wind Dispersion Logic
    # inputs shape: [Batch, 12, 8]
    # Feature index 3 is 'windspeed_kph'
    # Feature index 6 is 'pm2_5_lag1' (previous pollution)

    wind_norm = inputs[:, -1, 3]     # Get wind speed of the last time step
    prev_pollution = inputs[:, -1, 6] # Get pollution of the last time step

    # PHYSICS HYPOTHESIS:
    # If wind is strong (>0.5 normalized, approx >10km/h),
    # pollution should not increase drastically.
    # We penalize the model if it predicts an increase when wind is high.

    diff = pred.squeeze() - prev_pollution # Change in pollution
    penalty = torch.mean(torch.relu(diff) * wind_norm)

    # Total Loss = Data Fit + Physics Constraint
    total_loss = mse_loss + 0.05 * penalty
    return total_loss, mse_loss, penalty

