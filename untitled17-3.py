# 2. Training Loop
num_epochs = 30 # Number of times the model sees the entire dataset

print("Starting Training...")
loss_history = []

for epoch in range(num_epochs):
    model.train() # Set model to training mode

    epoch_loss = 0
    epoch_mse = 0
    epoch_phys = 0

    for X_batch, y_batch in loader:
        # Move batch to GPU
        X_batch, y_batch = X_batch.to(device), y_batch.to(device)

        # Clear gradients from previous step
        optimizer.zero_grad()

        # Forward pass: Compute predicted output
        outputs = model(X_batch)

        # Compute Loss (MSE + Physics Penalty)
        loss, mse, phys = physics_loss(outputs, y_batch, X_batch)

        # Backward pass: Compute gradient of the loss w.r.t. model parameters
        loss.backward()

        # Update parameters
        optimizer.step()

        epoch_loss += loss.item()
        epoch_mse += mse.item()
        epoch_phys += phys.item()

    # Calculate average loss for the epoch
    avg_loss = epoch_loss / len(loader)
    avg_mse = epoch_mse / len(loader)
    avg_phys = epoch_phys / len(loader)

    loss_history.append(avg_loss)

    # Print progress every 5 epochs
    if (epoch+1) % 5 == 0:
        print(f'Epoch [{epoch+1}/{num_epochs}], Total Loss: {avg_loss:.6f}, MSE: {avg_mse:.6f}, Physics Penalty: {avg_phys:.6f}')

print("Training Complete!")

